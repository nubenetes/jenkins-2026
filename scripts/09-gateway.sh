#!/usr/bin/env bash
# Exposes Jenkins, ArgoCD, Microservices and Headlamp on the public internet via the GKE
# Gateway API: one global external HTTPS Gateway (gke-l7-global-external-managed,
# using the static IP + managed certificate from terraform/gateway-bootstrap),
# one HTTPRoute per app, and a GCPBackendPolicy enabling Identity-Aware Proxy
# (IAP) in front of the Jenkins and Headlamp Services only - Microservices stays
# public. See README.md "Public access (GKE Gateway API + IAP)".
set -euo pipefail

source "$(dirname "${BASH_SOURCE[0]}")/lib/common.sh"
source "$(dirname "${BASH_SOURCE[0]}")/lib/config.sh"

if [[ -z "${J2026_GATEWAY_BASE_DOMAIN}" ]]; then
  log_info "gateway.baseDomain / JENKINS2026_BASE_DOMAIN is empty - gateway disabled, skipping."
  log_info "(set it, after running the Day0.infra.01-gateway bootstrap workflow once, to enable public access)"
  exit 0
fi

if [[ "${J2026_PLATFORM}" != "gke" ]]; then
  log_info "platform.target='${J2026_PLATFORM}' - the gke-l7-global-external-managed"
  log_info "GatewayClass and GCPBackendPolicy are GKE-specific, skipping."
  exit 0
fi

GENERATED_DIR="${J2026_ROOT_DIR}/.generated/gateway"
mkdir -p "${GENERATED_DIR}"

# Backend TLS (LB→pod re-encryption, docs/504) - resolved ONCE: flag AND the
# BackendTLSPolicy CRD (j2026_backend_tls_active, lib/common.sh). Drives the
# per-TLS-ready-backend BackendTLSPolicy + HTTPS HealthCheckPolicy blocks below.
BACKEND_TLS_ACTIVE="$(j2026_backend_tls_active)"
# argocd-server has a SECOND gate: its deploy caller must speak TLS (jenkins/
# githubactions only). Drives the argocd BackendTLSPolicy + HTTPS-vs-HTTP health
# check below, so tekton/argo clusters keep a plaintext argocd + HTTP probe.
ARGOCD_TLS_ACTIVE="$(j2026_argocd_backend_tls_active)"

# Fixed names
J2026_GATEWAY_HOST_ARGOCD="argocd"
export J2026_GATEWAY_ARGOCD_HOST="${J2026_GATEWAY_HOST_ARGOCD}.${J2026_GATEWAY_BASE_DOMAIN}"

log_step "Generating Gateway (static IP '${J2026_GATEWAY_STATIC_IP_NAME}', certmap '${J2026_GATEWAY_CERTMAP_NAME}')"
cat >"${GENERATED_DIR}/gateway.yaml" <<EOT
# Generated by scripts/09-gateway.sh from config/config.yaml - do not edit by
# hand, do not commit (see .gitignore).
apiVersion: gateway.networking.k8s.io/v1
kind: Gateway
metadata:
  name: ${J2026_GATEWAY_NAME}
  namespace: ${J2026_GATEWAY_NAMESPACE}
  annotations:
    networking.gke.io/certmap: ${J2026_GATEWAY_CERTMAP_NAME}
spec:
  gatewayClassName: gke-l7-global-external-managed
  addresses:
    - type: NamedAddress
      value: ${J2026_GATEWAY_STATIC_IP_NAME}
  listeners:
    - name: https
      protocol: HTTPS
      port: 443
      tls:
        mode: Terminate
        options:
          networking.gke.io/pre-shared-certs: ""
      allowedRoutes:
        namespaces:
          from: All
EOT

log_step "Generating HTTPRoutes (argocd, microservices, headlamp; jenkins only when ci.engine=jenkins)"
# Jenkins HTTPRoute only when Jenkins is the CI engine - the other engines retire
# the Jenkins Service (tekton/githubactions/argoworkflows), so route to it ONLY for
# ci.engine=jenkins (an explicit == check, not != tekton, so githubactions and
# argoworkflows - which also have no Jenkins Service - emit no jenkins route).
if [[ "${J2026_CI_ENGINE}" == "jenkins" ]]; then
