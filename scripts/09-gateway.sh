    - name: ${J2026_GATEWAY_NAME}
      namespace: ${J2026_GATEWAY_NAMESPACE}
      sectionName: https
  hostnames:
    - "${J2026_GATEWAY_JENKINS_HOST}"
  rules:
    - backendRefs:
        - name: ${J2026_JENKINS_RELEASE}
          port: 8080
EOT
fi

